void drawMaze() {
    for (int i = 1; i <= mazeWidth; i++) {
        for (int j = 1; j <= mazeHeight; j++) {
            if (maze[j-1][i-1] == 1) {
                Color customColor = (Color){9, 6, 96, 255};
                DrawRectangle(i * cellSize, j * cellSize, cellSize, cellSize, customColor);
            }
        }
    }
}

