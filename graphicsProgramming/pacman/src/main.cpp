    InitWindow(screenWidth, screenHeight, "Pac-Man Game");
    SetTargetFPS(60);

    while (!WindowShouldClose()) {
        BeginDrawing();
        Color customColor = (Color){3, 6, 9, 255};
        ClearBackground(customColor);

        drawMaze();

        EndDrawing();
    }

    CloseWindow();
    return 0;
